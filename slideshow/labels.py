def _chunk(chunk_type: bytes, data: bytes) -> bytes:
    payload = chunk_type + data
    crc = struct.pack(">I", zlib.crc32(payload) & 0xFFFFFFFF)
    return struct.pack(">I", len(data)) + payload + crc


def _write_rgba_png(path: Path, width: int, height: int, pixels: bytes) -> None:
    rows = []
    stride = width * 4
    for y in range(height):
        rows.append(b"\x00" + pixels[y * stride : (y + 1) * stride])
    compressed = zlib.compress(b"".join(rows), level=9)
    ihdr = struct.pack(">IIBBBBB", width, height, 8, 6, 0, 0, 0)
    png = b"\x89PNG\r\n\x1a\n"
    png += _chunk(b"IHDR", ihdr)
    png += _chunk(b"IDAT", compressed)
    png += _chunk(b"IEND", b"")
    path.write_bytes(png)


def render_label_png(
    text: str,
    path: Path,
    *,
    scale: int = 4,
    pad: int = 12,
    fg: tuple[int, int, int, int] = (255, 255, 255, 255),
    bg: tuple[int, int, int, int] = (0, 0, 0, 160),
) -> Path:
    """Render text to a translucent PNG label and return its path."""
    chars = [ch if ch in _GLYPHS else "?" for ch in text]
    glyph_w, glyph_h = 5, 7
    gap = 1
    text_w = len(chars) * (glyph_w + gap) - gap if chars else 0
    width = text_w * scale + pad * 2
    height = glyph_h * scale + pad * 2
    width += width % 2
    height += height % 2

    pixels = bytearray()
    pixels.extend(bytes(bg) * (width * height))

    def put(x: int, y: int, color: tuple[int, int, int, int]) -> None:
        if 0 <= x < width and 0 <= y < height:
            i = (y * width + x) * 4
            pixels[i : i + 4] = bytes(color)

    cursor_x = pad
    for ch in chars:
        rows = _GLYPHS[ch]
        for gy, row in enumerate(rows):
            for gx, bit in enumerate(row):
                if bit != "1":
                    continue
                for sy in range(scale):
                    for sx in range(scale):
                        put(cursor_x + gx * scale + sx, pad + gy * scale + sy, fg)
        cursor_x += (glyph_w + gap) * scale

    path.parent.mkdir(parents=True, exist_ok=True)
    _write_rgba_png(path, width, height, bytes(pixels))
    return path
